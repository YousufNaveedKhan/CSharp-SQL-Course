class Program
{
    static void Main(string[] args)
    {
        // Declare variables of different data types
        char myChar= 'A';
        string myString= "Apple";
        int myInt= 23;
        double myDouble= 6.1;
        bool myBool= true;

        // Print the value of each variable
        Console.WriteLine("Value of char variable: " + myChar);
        Console.WriteLine("Value of string variable: " + myString);
        Console.WriteLine("Value of int variable: " + myInt);
        Console.WriteLine("Value of double variable: " + myDouble);
        Console.WriteLine("Value of bool variable: " + myBool);
    }
}